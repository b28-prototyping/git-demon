pub fn draw_char(
    fb: &mut ImageBuffer<Rgba<u8>, Vec<u8>>,
    ch: char,
    x: u32,
    y: u32,
    color: Rgba<u8>,
    scale: u32,
) {
    let idx = ch as u32;
    if !(32..=126).contains(&idx) {
        return;
    }
    let glyph_offset = ((idx - 32) as usize) * GLYPH_H as usize;

    let (fw, fh) = (fb.width(), fb.height());

    for row in 0..GLYPH_H {
        let bits = FONT_DATA[glyph_offset + row as usize];
        for col in 0..GLYPH_W {
            if bits & (0x80 >> col) != 0 {
                for sy in 0..scale {
                    for sx in 0..scale {
                        let px = x + col * scale + sx;
                        let py = y + row * scale + sy;
                        if px < fw && py < fh {
                            fb.put_pixel(px, py, color);
                        }
                    }
                }
            }
        }
    }
}

pub fn draw_text(
    fb: &mut ImageBuffer<Rgba<u8>, Vec<u8>>,
    text: &str,
    x: u32,
    y: u32,
    color: Rgba<u8>,
    scale: u32,
) {
    let spacing = (GLYPH_W + 1) * scale;
    for (i, ch) in text.chars().enumerate() {
        draw_char(fb, ch, x + i as u32 * spacing, y, color, scale);
    }
}

pub fn text_width(text: &str, scale: u32) -> u32 {
    let count = text.len() as u32;
    if count == 0 {
        return 0;
    }
    count * (GLYPH_W + 1) * scale - scale
}

#[cfg(test)]
mod tests {
    use super::*;
    use image::{ImageBuffer, Rgba};

    const WHITE: Rgba<u8> = Rgba([255, 255, 255, 255]);
    const BLACK: Rgba<u8> = Rgba([0, 0, 0, 0]);

    fn is_set(fb: &ImageBuffer<Rgba<u8>, Vec<u8>>, x: u32, y: u32) -> bool {
        *fb.get_pixel(x, y) == WHITE
    }

    fn make_fb(w: u32, h: u32) -> ImageBuffer<Rgba<u8>, Vec<u8>> {
        ImageBuffer::from_pixel(w, h, BLACK)
    }

    #[test]
    fn test_glyph_a_uppercase() {
        // 'A' pattern:
        // .###.  0x70
        // #...#  0x88
        // #...#  0x88
        // #####  0xF8
        // #...#  0x88
        // #...#  0x88
        // #...#  0x88
        let mut fb = make_fb(5, 7);
        draw_char(&mut fb, 'A', 0, 0, WHITE, 1);

        // Row 0: .###. — cols 1,2,3 set
        assert!(!is_set(&fb, 0, 0));
        assert!(is_set(&fb, 1, 0));
        assert!(is_set(&fb, 2, 0));
        assert!(is_set(&fb, 3, 0));
        assert!(!is_set(&fb, 4, 0));

        // Row 3: ##### — all 5 cols set
        for col in 0..5 {
            assert!(is_set(&fb, col, 3), "col {} row 3 should be set", col);
        }

        // Row 1: #...# — cols 0,4 set
        assert!(is_set(&fb, 0, 1));
        assert!(!is_set(&fb, 1, 1));
        assert!(!is_set(&fb, 2, 1));
        assert!(!is_set(&fb, 3, 1));
        assert!(is_set(&fb, 4, 1));
    }

    #[test]
    fn test_glyph_zero() {
        // '0' pattern:
        // .###.  0x70
        // #...#  0x88
        // ##..#  0x98? — actually 0x98 = 10011000 → bits 7,4,3 → cols 0,3,4? No.
        // Wait: 0x98 = 1001_1000 → bit7=1, bit6=0, bit5=0, bit4=1, bit3=1 → cols 0,3,4
        // That's #..## which represents the diagonal stroke in '0'
        let mut fb = make_fb(5, 7);
        draw_char(&mut fb, '0', 0, 0, WHITE, 1);

        // Row 0: .###. — top of oval
        assert!(!is_set(&fb, 0, 0));
        assert!(is_set(&fb, 1, 0));
        assert!(is_set(&fb, 2, 0));
        assert!(is_set(&fb, 3, 0));
        assert!(!is_set(&fb, 4, 0));

        // Row 6: .###. — bottom of oval (same as top, 0x70)
        assert!(!is_set(&fb, 0, 6));
        assert!(is_set(&fb, 1, 6));
        assert!(is_set(&fb, 2, 6));
        assert!(is_set(&fb, 3, 6));
        assert!(!is_set(&fb, 4, 6));

        // Row 1: #...# — sides
        assert!(is_set(&fb, 0, 1));
        assert!(is_set(&fb, 4, 1));
    }

    #[test]
    fn test_glyph_exclamation() {
        // '!' pattern:
        // .#...  0x20
        // .#...  0x20
        // .#...  0x20
        // .#...  0x20
        // .....  0x00
        // .#...  0x20
        // .....  0x00
        let mut fb = make_fb(5, 7);
        draw_char(&mut fb, '!', 0, 0, WHITE, 1);

        // Rows 0–3: col 1 is set (bit 6 = 0x40? No, 0x20 = bit 5 = col 2)
        // 0x20 = 0010_0000 → bit5=1 → col 2
        for row in 0..4 {
            assert!(is_set(&fb, 2, row), "row {} col 2 should be set", row);
        }
        // Row 4: blank
        for col in 0..5 {
            assert!(!is_set(&fb, col, 4), "row 4 should be blank");
        }
        // Row 5: col 2 set (the dot)
        assert!(is_set(&fb, 2, 5));
    }

    #[test]
    fn test_text_width() {
        assert_eq!(text_width("", 1), 0);
        assert_eq!(text_width("A", 1), 5); // 1 * 6 - 1 = 5
        assert_eq!(text_width("AB", 1), 11); // 2 * 6 - 1 = 11
        assert_eq!(text_width("A", 2), 10); // 1 * 12 - 2 = 10
        assert_eq!(text_width("AB", 2), 22); // 2 * 12 - 2 = 22
    }

    #[test]
    fn test_draw_text_spacing() {
        let mut fb = make_fb(20, 7);
        draw_text(&mut fb, "AB", 0, 0, WHITE, 1);

        // 'A' occupies cols 0–4, gap at col 5, 'B' starts at col 6
        // 'B' row 0 = 0xF0 = #####. wait no, 0xF0 = 1111_0000 → bits 7,6,5,4 → cols 0,1,2,3
        // So 'B' at x=6: cols 6,7,8,9 should be set at row 0
        assert!(is_set(&fb, 6, 0)); // B col 0
        assert!(is_set(&fb, 7, 0)); // B col 1
        assert!(is_set(&fb, 8, 0)); // B col 2
        assert!(is_set(&fb, 9, 0)); // B col 3
        assert!(!is_set(&fb, 5, 0)); // gap between A and B
    }

    #[test]
    fn test_space_is_blank() {
        let mut fb = make_fb(5, 7);
        draw_char(&mut fb, ' ', 0, 0, WHITE, 1);

        for y in 0..7 {
            for x in 0..5 {
                assert!(!is_set(&fb, x, y), "space should have no pixels set");
            }
        }
    }
}
